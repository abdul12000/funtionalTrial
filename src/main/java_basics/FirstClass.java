package java_basics;

public class FirstClass {

    public static void main(String[] args) {

        //Short cut to format screen is ctrl+WindowsKey+Alt+L

        //Declaring variables
        int a, b, c, d, e, f, g ;

        //initializing the variables
        a=9;
        b=9;

//printing out
//addition operation
        g =a+b;
        System.out.println("Addition of a and b : " + (g));

        //subtraction operation
        c=a-b;
        System.out.println("subtracting b from a : " + c);

//multiplication operation
        d =a*b;
        System.out.println("multiplication of a and b : " + d);

//division operation
        e =a/b;
        System.out.println(e);


//Modulo or Remainder operation
        f =a%b;
        System.out.println("Remainder of a by b : " + f);


        //printout hello world
        System.out.println("Hello World");
    }

}
