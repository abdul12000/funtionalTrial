package java_basics;

public class SeventhClass {

    public static void main(String[] args) {
        //While loop
        int j = 6;
        while (j < 11) {

            System.out.println(j);
            //increment
            j++;
        }

        //Do-While Loop
        int i = 1;
        do {
            System.out.println("value of i is : " + i);
            i++;
        }
        while (i < 10);

//show decrement with the while loop
        int k = 16;
        while (k > 11) {

            System.out.println(k);
            //increment
            k--;
        }

    }
}
